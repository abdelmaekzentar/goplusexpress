function simExpress(direction, country, type, weightNet, l, w, h) {
  // Calcul poids taxable
  let weightTax = weightNet;
  if(type !== 'document') {
    const vol = (l * w * h) / 5000;
    weightTax = Math.max(weightNet, vol);
  }
  weightTax = Math.ceil(weightTax * 2) / 2; // arrondir au 0.5 supérieur

  const fuel = {dhl:0.46, fedex:0.48, aramex:0.47};

  // Prix hors fuel
  const dhlBase = getDHLPrice(direction, country, weightTax);
  const fedexBase = getFedExPrice(direction, country, type === 'colis' ? 'package' : (type === 'palette' ? 'package' : 'document'), weightTax);
  const aramexBase = getAramexPrice(direction, country, weightTax);

  const result = {
    weightTax,
    transit: TRANSIT_TIMES[country] || '—',
    carriers: {
      dhl: dhlBase ? {
        base: dhlBase,
        fuelAmt: Math.round(dhlBase * fuel.dhl),
        total: Math.round(dhlBase * (1 + fuel.dhl)),
        zone: 'Z' + getDHLZone(country)
      } : null,
      fedex: fedexBase ? {
        base: fedexBase,
        fuelAmt: Math.round(fedexBase * fuel.fedex),
        total: Math.round(fedexBase * (1 + fuel.fedex)),
        zone: getFedExZone(country)
      } : null,
      aramex: aramexBase ? {
        base: aramexBase,
        fuelAmt: Math.round(aramexBase * fuel.aramex),
        total: Math.round(aramexBase * (1 + fuel.aramex)),
        zone: getAramexZone(country)
      } : null
    }
  };
  return result;
}
