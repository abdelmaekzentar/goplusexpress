function getFedExPrice(direction, country, type, weightKg) {
  const zone = getFedExZone(country);
  if(direction === 'export') {
    if(type === 'document') {
      // DOC fixe à 0.5 kg (enveloppe)
      return FEDEX_EXP_DOC[zone] || null;
    } else if(type === 'pak') {
      // PAK 0.5-2.5 kg
      const idx = Math.min(Math.ceil(weightKg/0.5)-1, FEDEX_EXP_PAK_W.length-1);
      return (FEDEX_EXP_PAK[zone] || [])[Math.max(0,idx)] || null;
    } else {
      // PACKAGE
      const weights = FEDEX_EXP_PKG_WEIGHTS;
      let idx = weights.findIndex(w => w >= weightKg);
      if(idx < 0) idx = weights.length - 1;
      return (FEDEX_EXP_PKG[zone] || [])[idx] || null;
    }
  } else {
    // import
    if(type === 'document' || type === 'pak') {
      const idx = Math.min(Math.ceil(weightKg/0.5)-1, FEDEX_IMP_DOCPAK_W.length-1);
      return (FEDEX_IMP_DOCPAK[zone] || [])[Math.max(0,idx)] || null;
    } else {
      const idx = Math.min(Math.ceil(weightKg/0.5)-1, FEDEX_IMP_PKG_W.length-1);
      return (FEDEX_IMP_PKG[zone] || [])[Math.max(0,idx)] || null;
    }
  }
}

