function getAramexPrice(direction, country, weightKg) {
  const zone = direction === 'export' ? getAramexZone(country) : (()=>{
    // Import: pas d'overrides pays, zones Z1-Z8 seulement
    const z = getAramexZone(country);
    // Remapper les overrides vers zones de base pour l'import
    const mapToBase = {BE:"Z5",ES:"Z5",FR:"Z5",NL:"Z5",TR:"Z7",US:"Z1",GB:"Z3"};
    return mapToBase[z] || z;
  })();
  const table = direction === 'export' ? ARAMEX_EXP : ARAMEX_IMP;
  const ksupp = direction === 'export' ? ARAMEX_EXP_KSUPP : ARAMEX_IMP_KSUPP;
  const prices = table[zone];
  if(!prices) return null;
  if(weightKg <= 70) {
    let idx = ARAMEX_WEIGHTS.findIndex(w => w >= weightKg);
    if(idx < 0) idx = ARAMEX_WEIGHTS.length - 1;
    return prices[idx] || null;
  } else {
    return prices[prices.length-1] + Math.ceil(weightKg-70)*ksupp[zone];
  }
}

