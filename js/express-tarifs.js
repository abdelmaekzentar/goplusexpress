function getDHLPrice(direction, country, weightKg) {
  const zone = getDHLZone(country);
  const table = direction === 'export' ? DHL_EXP : DHL_IMP;
  const ksupp = direction === 'export' ? DHL_EXP_KSUPP : DHL_IMP_KSUPP;
  const prices = table[zone];
  if (!prices) return null;
  if (weightKg <= 70) {
    // trouver l'index correspondant au poids (arrondi au 0.5 supérieur)
    const idx = Math.min(Math.ceil(weightKg / 0.5) - 1, prices.length - 1);
    return prices[Math.max(0, idx)];
  } else {
    // kilo supp au-delà de 70 kg
    return prices[prices.length - 1] + Math.ceil(weightKg - 70) * ksupp[zone];
  }
}

